-- TOP SELLING PRODUCTS

SELECT P.NAME AS PRODUCT_NAME,SUM(OI.QUANTITY) AS TOTAL_QUANTITY_SOLD
FROM ORDER_ITEMS OI
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.NAME
ORDER BY TOTAL_QUANTITY_SOLD DESC;


-- MOST VALUABLE CUSTOMERS

SELECT C.NAME AS CUSTOMER_NAME,SUM(OI.QUANTITY * P.PRICE) AS TOTAL_SPENT
FROM CUSTOMERS C
JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDER_ITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY C.NAME
ORDER BY TOTAL_SPENT DESC;


-- MONTHLY REVENUE CALCULATION

SELECT YEAR(O.ORDER_DATE) AS YEAR, MONTH(O.ORDER_DATE) AS MONTH,
	SUM(OI.QUANTITY * P.PRICE) AS MONTHLY_REVENUE
FROM ORDERS O
JOIN ORDER_ITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY YEAR(O.ORDER_DATE),MONTH(O.ORDER_DATE)
ORDER BY YEAR,MONTH;


-- CATEGORY-WISE SALES ANALYSIS

SELECT P.CATEGORY,SUM(OI.QUANTITY * P.PRICE) AS CATEGORY_REVENUE
FROM ORDER_ITEMS OI
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.CATEGORY
ORDER BY CATEGORY_REVENUE DESC;


-- INACTIVE CUSTOMERS

SELECT C.NAME AS INACTIVE_CUSTOMER
FROM CUSTOMERS C
LEFT JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
AND O.ORDER_DATE >= DATE_SUB(CURDATE(),INTERVAL 2 MONTH)
WHERE O.ORDER_ID IS NULL;


	